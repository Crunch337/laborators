#include <iostream>
#include <fstream>
using namespace std;

const int MAX_TEXT = 10000;

int main() {
    // Объявление всех переменных
    const char* filename = "C:\\Users\\user\\Desktop\\ЛАБЫ\\2 семестр\\input.txt";
    char text[MAX_TEXT] = { 0 };              // массив для текста (обнулён)
    int len = 0;                            // длина текста
    char ch = 0;                             // вспомогательный символ(используется при чтении из файла)
    int best_start = 0;                      // начало предложения с наибольшим количесвтом цифр
    int best_end = -1;                        // конец предложения с наибольшим количеством цифр
    int best_digits = -1;                     // количество цифр в предложении с наибольшим количсевтом цифр
    int cur_start = 0;                         // начало текущего предложения
    int cur_digits = 0;                        // цифр в текущем
    int i = 0;                                  // счётчик для циклов
    int digit = 0;                              // номер цифры (0..9)
    int word_len = 0;                           // длина числительного
    int shift = 0;                              // величина сдвига
    char digit_names[10][10] = {                // массив названий цифр
        "ноль", "один", "два", "три", "четыре",
        "пять", "шесть", "семь", "восемь", "девять"
    };

    // Открытие файла и проверка на его существование
    ifstream file(filename);
    if (!file)
    {
        cout << "Ошибка: файл '" << filename << "' не найден.\n";
        cout << "Убедитесь, что файл существует, и повторите попытку.\n";
        return 1;
    }

    // Чтение файла в массив text 
    while (file.get(ch) && len < MAX_TEXT - 1)
    {
        text[len] = ch;
        len++;
    }
    text[len] = '\0';// завершающий нуль
    file.close();//закрываем файл

    // Проверка, не пуст ли файл 
    if (len == 0)
    {
        cout << "Файл пуст.\n";
        return 1;
    }

    // 1: поиск предложения с максимумом цифр 
    for (i = 0; i < len; i++)
    {
        // проверка, является ли символ цифрой
        if (text[i] >= '0' && text[i] <= '9')
            cur_digits++;

        // если достигнут конец предложения
        if (text[i] == '.' || text[i] == '!' || text[i] == '?')
        {
            // если текущеее количество цифр больше количества цифр в наибольшем
            if (cur_digits > best_digits)
            {
                best_digits = cur_digits;
                best_start = cur_start;
                best_end = i;
            }
            // начинаем новое предложение
            cur_start = i + 1;
            cur_digits = 0;
        }
    }

    // проверка последнего фрагмента(нужна если последнее предложение не заканчивается разделителем)
    if (cur_start < len)
    {
        cur_digits = 0;
        // считаем количество цифр в последнем предложении
        for (i = cur_start; i < len; i++)
            // проверка на цифру
            if (text[i] >= '0' && text[i] <= '9')
                cur_digits++;
        // если количество цифр в текущем больше, то оно становится лучшим
        if (cur_digits > best_digits)
        {
            best_digits = cur_digits;
            best_start = cur_start;
            best_end = len - 1;
        }
    }

    // вывод результата 
    if (best_end >= 0)
    {
        cout << "Предложение с наибольшим количеством цифр:\n";
        for (i = best_start; i <= best_end; i++)
            cout << text[i];
        cout << endl;
    }
    else
    {
        cout << "В файле нет ни одного предложения.\n";
    }

    // 2: замена цифр на числительные в том же массиве 
    for (i = 0; i < len; )
    {
        if (text[i] >= '0' && text[i] <= '9')
        {
            digit = text[i] - '0';// номер цифры 

            // вычисляем длину числительного
            word_len = 0;
            while (digit_names[digit][word_len] != '\0')
                word_len++;

            shift = word_len - 1;// на сколько позиций сдвинуть хвост

            // сдвигаем хвост строки вправо на shift
            for (int j = len; j > i; j--)
            {
                text[j + shift] = text[j];
            }

            // вставляем слово на освободившееся место
            for (int k = 0; k < word_len; k++)
            {
                text[i + k] = digit_names[digit][k];
            }

            // обновляем длину строки
            len += shift;

            // перемещаем индекс на позицию после вставленного слова
            i += word_len;
        }
        else
        {
            // обычный символ - просто идём дальше
            i++;
        }
    }
    text[len] = '\0'; // обновляем завершающий нуль

    // вывод результата второй части
    cout << "\nТекст после замены цифр на числительные:\n";
    cout << text << endl;

    return 0;
}